class Producto{
    constructor(id, nombre, autor, editorial, descripcion, precio, tipoPasta, genero, cantidad, portada){
        this.id = id;
        this.nombre = nombre;
        this.autor = autor;
        this.editorial = editorial;
        this.descripcion = descripcion;
        this.precio = precio;
        this.tipoPasta = tipoPasta;
        this.genero = genero;
        this.cantidad = cantidad;
        this.portada = portada;
    }
};

export default Producto;